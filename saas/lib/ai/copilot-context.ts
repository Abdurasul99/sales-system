function translateRole(role: string): string {
  const map: Record<string, string> = {
    SUPERADMIN: "Суперадминистратор",
    ADMIN: "Администратор",
    CASHIER: "Кассир",
    WAREHOUSE_CLERK: "Кладовщик",
    SUPPLIER_CONTACT: "Поставщик",
  };
  return map[role] ?? role;
}

function buildRestrictionNote(user: UserContext): string {
  const lines: string[] = [];
  if (user.role === "CASHIER") {
    lines.push("⚠️ Кассир имеет доступ только к: Продажи, Смены, Уведомления");
  }
  if (user.role === "WAREHOUSE_CLERK") {
    lines.push("⚠️ Кладовщик имеет доступ только к: Склад, Поступления, Списания, Уведомления");
  }
  if (!user.planName || user.planName === "Basic") {
    lines.push("ℹ️ Текущий план Basic: ограничен 200 товарами, 3 сотрудниками, аналитикой за 7 дней");
  }
  return lines.join("\n");
}

function buildPageDataSummary(page: string, data: Record<string, unknown>): string {
  try {
    switch (page) {
      case "analytics": {
        const d = data as {
          revenue?: number; expenses?: number; profit?: number;
          salesCountToday?: number; salesCountWeek?: number; salesCountMonth?: number;
          lowStockCount?: number; cancelledSales?: number;
          topProducts?: { name: string; total: number }[];
        };
        return [
          `Выручка за месяц: ${fmtNum(d.revenue)} сум`,
          `Расходы за месяц: ${fmtNum(d.expenses)} сум`,
          `Прибыль: ${fmtNum(d.profit)} сум`,
          `Продаж сегодня: ${d.salesCountToday ?? 0}`,
          `Продаж за неделю: ${d.salesCountWeek ?? 0}`,
          `Продаж за месяц: ${d.salesCountMonth ?? 0}`,
          `Товаров с низким остатком: ${d.lowStockCount ?? 0}`,
          `Отменённых продаж: ${d.cancelledSales ?? 0}`,
          d.topProducts?.length
            ? `Топ товар: ${d.topProducts[0]?.name} (${fmtNum(d.topProducts[0]?.total)} сум)`
            : "",
        ].filter(Boolean).join("\n");
      }
      case "warehouse": {
        const d = data as { lowStockItems?: { name: string; qty: number; min: number }[]; totalItems?: number };
        return [
          `Всего позиций: ${d.totalItems ?? 0}`,
          d.lowStockItems?.length
            ? `Заканчиваются: ${d.lowStockItems.slice(0, 3).map(i => `${i.name} (${i.qty}/${i.min})`).join(", ")}`
            : "Нет товаров с критическим остатком",
        ].join("\n");
      }
      case "expenses": {
        const d = data as { monthTotal?: number; monthCount?: number };
        return `Расходов за месяц: ${fmtNum(d.monthTotal)} сум (${d.monthCount ?? 0} записей)`;
      }
      default:
        return "";
    }
  } catch {
    return "";
  }
}

function fmtNum(n?: number): string {
  if (n === undefined || n === null) return "0";
  return new Intl.NumberFormat("ru-RU").format(Math.round(n));
}

